package child

import (
	"bytes"
	"context"
	"errors"
	"fmt"
	"net/http"
	"strings"

	"go.skia.org/infra/autoroll/go/config_vars"
	"go.skia.org/infra/autoroll/go/revision"
	"go.skia.org/infra/go/depot_tools/deps_parser"
	"go.skia.org/infra/go/gitiles"
	"go.skia.org/infra/go/skerr"
)

const (
	GitilesRevTmpl = "%s/+/%s"
)

// GitilesConfig provides configuration for gitilesChild.
type GitilesConfig struct {
	// Branch is the name of the git branch to be rolled.
	Branch *config_vars.Template `json:"branch"`
	// RepoURL is the git repository URL.
	RepoURL string `json:"repoURL"`
	// Dependencies is an optional mapping of dependency ID (eg. repo URL)
	// to the location within the repo where its version is pinned (eg. DEPS).
	// Revisions generated by the Child will contain the versions of these
	// dependencies. The values must be either files whose entire contents
	// are the version ID of the dependency, or "DEPS" which is a special
	// case.
	Dependencies map[string]string `json:"dependencies,omitempty"`
}

// See documentation for util.Validator interface.
func (c *GitilesConfig) Validate() error {
	if c.Branch == nil {
		return errors.New("Branch is required.")
	}
	if err := c.Branch.Validate(); err != nil {
		return skerr.Wrap(err)
	}
	if c.RepoURL == "" {
		return errors.New("RepoURL is required.")
	}
	return nil
}

// NewGitiles returns an implementation of Child which uses Gitiles rather
// than a local checkout.
func NewGitiles(ctx context.Context, c GitilesConfig, reg *config_vars.Registry, client *http.Client) (Child, error) {
	if err := c.Validate(); err != nil {
		return nil, skerr.Wrap(err)
	}
	if err := reg.Register(c.Branch); err != nil {
		return nil, skerr.Wrap(err)
	}
	repo := gitiles.NewRepo(c.RepoURL, client)
	return &gitilesChild{
		branch: c.Branch,
		deps:   c.Dependencies,
		repo:   repo,
	}, nil
}

// gitilesChild is an implementation of Child which uses Gitiles rather than a
// local checkout.
type gitilesChild struct {
	branch *config_vars.Template
	deps   map[string]string
	repo   *gitiles.Repo
}

// See documentation for Child interface.
func (c *gitilesChild) GetRevision(ctx context.Context, id string) (*revision.Revision, error) {
	// Load the details for this revision.
	details, err := c.repo.Details(ctx, id)
	if err != nil {
		return nil, skerr.Wrapf(err, "Failed to retrieve revision %q", id)
	}
	rev := revision.FromLongCommit(fmt.Sprintf(GitilesRevTmpl, c.repo.URL, "%s"), details)

	// Optionally load any dependencies.
	if len(c.deps) > 0 {
		rev.Dependencies = make(map[string]string, len(c.deps))
		var depsEntries map[string]*deps_parser.DepsEntry
		for dep, path := range c.deps {
			if path == deps_parser.DepsFileName {
				// Lazily load the DEPS entries.
				if depsEntries == nil {
					var err error
					depsEntries, err = deps_parser.FromGitiles(ctx, c.repo, rev.Id)
					if err != nil {
						return nil, skerr.Wrap(err)
					}
				}

				// Find the desired DEPS entry.
				entry, ok := depsEntries[dep]
				if !ok {
					return nil, skerr.Fmt("Dependency %q not found in DEPS!", dep)
				}
				rev.Dependencies[dep] = entry.Version
			} else {
				var buf bytes.Buffer
				if err := c.repo.ReadFileAtRef(ctx, path, id, &buf); err != nil {
					return nil, skerr.Wrap(err)
				}
				rev.Dependencies[dep] = strings.TrimSpace(buf.String())
			}
		}
	}
	return rev, nil
}

// See documentation for Child interface.
func (c *gitilesChild) Update(ctx context.Context, lastRollRev *revision.Revision) (*revision.Revision, []*revision.Revision, error) {
	tipRev, err := c.GetRevision(ctx, c.branch.String())
	if err != nil {
		return nil, nil, skerr.Wrapf(err, "Failed to retrieve tip rev")
	}
	notRolled, err := c.repo.LogLinear(ctx, lastRollRev.Id, tipRev.Id)
	if err != nil {
		return nil, nil, err
	}
	notRolledRevs := make([]*revision.Revision, 0, len(notRolled))
	for _, commit := range notRolled {
		rev, err := c.GetRevision(ctx, commit.Hash)
		if err != nil {
			return nil, nil, skerr.Wrapf(err, "Failed to retrieve revision")
		}
		notRolledRevs = append(notRolledRevs, rev)
	}
	return tipRev, notRolledRevs, nil
}
