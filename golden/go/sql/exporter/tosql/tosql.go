package main

import (
	"flag"
	"fmt"
	"io/ioutil"
	"path/filepath"
	"reflect"
	"strings"

	"os"

	"go.skia.org/infra/go/sklog"
	"go.skia.org/infra/golden/go/sql/schema"
)

func main() {
	outputFile := flag.String("output_file", "", "The name of the file to write to.")
	outputPkg := flag.String("output_pkg", "", "The name of the package to output to.")
	flag.Parse()
	cwd, err := os.Getwd()
	if err != nil {
		sklog.Fatalf("Could not get working dir")
	}
	sklog.Infof("Output dir %s. Output file is %s\n", cwd, *outputFile)

	generatedText, err := generateSQL(schema.Tables{}, *outputPkg)
	if err != nil {
		sklog.Fatalf("Could not create SQL: %s", err)
	}
	err = ioutil.WriteFile(filepath.Join(cwd, *outputFile), []byte(generatedText), 0666)
	if err != nil {
		sklog.Fatalf("Could not write SQL to file: %s", err)
	}
}

func generateSQL(inputType interface{}, pkg string) (string, error) {
	header := fmt.Sprintf("package %s\n\n// Generated by //golden/go/sql/exporter/tosql\n// DO NOT EDIT\n\nconst Schema = `", pkg)

	body := strings.Builder{}
	t := reflect.TypeOf(inputType)
	for i := 0; i < t.NumField(); i++ {
		field := t.Field(i)
		if field.Type.Kind() != reflect.Slice {
			continue
		}
		body.WriteString("CREATE TABLE IF NOT EXISTS ")
		body.WriteString(field.Name)
		body.WriteString(" (")
		row := field.Type.Elem()
		wasFirst := true
		for j := 0; j < row.NumField(); j++ {
			col := row.Field(j)
			sqlText, ok := col.Tag.Lookup("sql")
			if !ok {
				continue
			}
			if !wasFirst {
				body.WriteString(",")
			}
			wasFirst = false
			body.WriteString("\n  ")
			body.WriteString(sqlText)
		}
		body.WriteString("\n);\n")
	}
	const footer = "`\n"
	return header + body.String() + footer, nil
}
