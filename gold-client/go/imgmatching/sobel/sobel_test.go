func TestRotate_Success(t *testing.T) {
	unittest.SmallTest(t)

	test := func(name string, fnUnderTest func(*image.Gray) *image.Gray, input, expectedOutput *image.Gray) {
		t.Run(name, func(t *testing.T) {
			assertImagesEqual(t, expectedOutput, fnUnderTest(input))
		})
	}

	input := text.MustToGray(`! SKTEXTSIMPLE
	2 3
	0xAA 0xBB
	0xCC 0xDD
	0xEE 0xFF`)

	rotated90Degrees := text.MustToGray(`! SKTEXTSIMPLE
	3 2
	0xEE 0xCC 0xAA
	0xFF 0xDD 0xBB`)

	rotated180Degrees := text.MustToGray(`! SKTEXTSIMPLE
	2 3
	0xFF 0xEE
	0xDD 0xCC
	0xBB 0xAA`)

	rotated270Degrees := text.MustToGray(`! SKTEXTSIMPLE
	3 2
	0xBB 0xDD 0xFF
	0xAA 0xCC 0xEE`)

	test("rotate90", rotate90, input, rotated90Degrees)
	test("rotate180", rotate180, input, rotated180Degrees)
	test("rotate270", rotate270, input, rotated270Degrees)
}

// rotate90 returns the input image rotated by 90 degrees. It assumes the image starts at (0, 0).
func rotate90(img *image.Gray) *image.Gray {
	bounds := img.Bounds()

	// Fail loudly if the above assumption isn't met.
	if bounds.Min != (image.Point{X: 0, Y: 0}) {
		panic("rotate90 called with an image with a non-zero minimum bound")
	}

	rotatedImg := image.NewGray(image.Rect(0, 0, bounds.Max.Y, bounds.Max.X))
	for y := bounds.Min.Y; y <= bounds.Max.Y; y++ {
		for x := bounds.Min.X; x <= bounds.Max.X; x++ {
			rotatedImg.SetGray(bounds.Max.Y-y-1, x, img.GrayAt(x, y))
		}
	}
	return rotatedImg
}

// rotate90 returns the input image rotated by 180 degrees. It assumes the image starts at (0, 0).
func rotate180(img *image.Gray) *image.Gray {
	return rotate90(rotate90(img))
}

// rotate90 returns the input image rotated by 270 degrees. It assumes the image starts at (0, 0).
func rotate270(img *image.Gray) *image.Gray {
	return rotate90(rotate90(rotate90(img)))
}

// assertImagesEqual asserts that the two given images are equal, and prints out the actual image
// encoded as SKTEXT if the assertion is false.
func assertImagesEqual(t *testing.T, expected, actual image.Image) {
	assert.Equal(t, expected, actual, fmt.Sprintf("SKTEXT-encoded actual output:\n%s", imageToText(t, actual)))
}

// imageToText returns the given image as an SKTEXT-encoded string.
func imageToText(t *testing.T, img image.Image) string {
	// Convert image to NRGBA.
	nrgbaImg := image.NewNRGBA(img.Bounds())
	draw.Draw(nrgbaImg, img.Bounds(), img, img.Bounds().Min, draw.Src)

	// Encode and return image as SKTEXTSIMPLE.
	buf := &bytes.Buffer{}
	err := text.Encode(buf, nrgbaImg)
	require.NoError(t, err)
	return buf.String()
}
