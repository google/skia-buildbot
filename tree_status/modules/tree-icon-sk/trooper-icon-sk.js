import './tree-icon-sk.css';
import { define } from 'elements-sk/define'

const iconSkTemplate = document.createElement('template');
iconSkTemplate.innerHTML = '<svg class="icon-sk-svg" xmlns="http://www.w3.org/2000/svg" width=24 height=24 viewBox="0 0 24 24"><g id="trooper" transform="translate(0,-1028.3622)"></svg>';

define('trooper-icon-sk', class extends HTMLElement {
	connectedCallback() {
		let icon = iconSkTemplate.content.cloneNode(true);
    while (this.firstChild) {
      this.removeChild(this.firstChild);
    }
		this.appendChild(icon);
	}
});
