#!/bin/bash
# This script generates the install* scripts in this directory.
set -e

# Edit the PLATFORMS list when adding or removing a target platform.
PLATFORMS=(
  linux-amd64
  darwin-amd64
  windows-amd64
  linux-arm64
)

GO_DEPS_DIR="$( cd "$(dirname "$0")" >/dev/null 2>&1 ; pwd -P )"

# pkg_version retrieves the module version for a given package.
function pkg_version {
  GOOS="${1}" GOARCH="${2}" go list -f '{{if .Module}}{{if not .Module.Main}}{{.ImportPath}}@{{.Module.Version}}{{end}}{{end}}' $3
}

# write_script writes the given lines as a script.
function write_script {
  local ARGS=("${@}")
  local SCRIPT="${GO_DEPS_DIR}/${ARGS[0]}"
  echo ${SCRIPT}
  local LINES=("${ARGS[@]:1}")
  echo "LINES:"
  echo "${LINES[@]}"
  echo "^LINES"
  cat << EOF > ${SCRIPT}
#!/bin/bash
# This file is generated by ./generate.sh. DO NOT EDIT
set -e -x
EOF
  for ((i = 0; i < ${#LINES[@]}; i++)); do
    echo "LINE ${i}: ${LINES[$i]}"
    echo "${LINES[$i]}" >> ${SCRIPT}
  done
  chmod a+x ${SCRIPT}
}

# write_install_script writes a script to install packages.
function write_install_script {
  local SCRIPT="${1}"
  local ACTION="${2}"
  local PACKAGES="${3}"
  local LINES=("go ${ACTION} -v \\")
  for PACKAGE in ${PACKAGES}; do
    LINES+=("  ${PACKAGE} \\")
  done
  LINES+=(";")
  write_script "${SCRIPT}" "${LINES[@]}"
}

# install-tools.sh installs tools for the current platform.
TOOLS="$(cat ${GO_DEPS_DIR}/../tools.go | grep '_ "' | cut -d '"' -f 2)"
write_install_script "install-tools.sh" "install" "${TOOLS[@]}"
exit
# install-all.sh installs dependencies for all target platforms.
INSTALL_ALL_SCRIPT="${GO_DEPS_DIR}/install-all.sh"
cat << EOF > ${INSTALL_ALL_SCRIPT}
#!/bin/bash
# This file is generated by ./generate.sh. DO NOT EDIT
GO_DEPS_DIR="\$( cd "\$(dirname "\$0")" >/dev/null 2>&1 ; pwd -P )"
EOF
chmod a+x ${INSTALL_ALL_SCRIPT}

# Create the individual install-${PLATFORM} scripts.
for PLATFORM in ${PLATFORMS[@]}; do
  GOOS="$(echo ${PLATFORM} | cut -d '-' -f 1)"
  GOARCH="$(echo ${PLATFORM} | cut -d '-' -f 2)"
  SCRIPT="install-${PLATFORM}.sh"
  DEPS="$(pkg_version ${GOOS} ${GOARCH} all)"
  write_install_script "${SCRIPT}" "get" "${DEPS[@]}"

  # Add the current script to install-all.sh.
  echo "\${GO_DEPS_DIR}/${SCRIPT}" >> ${INSTALL_ALL_SCRIPT}
done